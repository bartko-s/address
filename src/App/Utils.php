    public static function createQuerySearchString(string $string): string
    {
        $string = trim($string);
        $string = self::removeDiacritics($string);

        return preg_replace('| +|', ' & ', $string);
    }

    public static function makeNGrams(string $string, int $minLength = 1): string
    {
        $result = [];

        foreach (explode(' ', $string) as $part) {
            $length = strlen($part);

            if ($minLength >= $length) {
                $result[] = $part;
            } else {
                for ($l = $minLength; $l <= $length; ++$l) {
                    for ($p = 0; $p <= $length; ++$p) {
                        if ($p + $l <= $length) {
                            $result[] = substr($part, $p, $l);
                        }
                    }
                }
            }
        }

        return implode(' ', $result);
    }
